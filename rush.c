/********************************************************************************/
/*		lcd_clearA																*/
/********************************************************************************/
void lcd_clearA(int x1, int y1, int x2, int y2, int color)
{
	int x, y;
	
	for(x=x1; x<=x2; x++){
		for(y=y1; y<=y2; y++){
			if(color==GRAY)
				Pset(x, y, (x^y)&1);
			else
				Pset(x, y, color);
		}
	}
}
