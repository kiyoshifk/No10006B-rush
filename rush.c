/********************************************************************************/
/*		rush_koma_disp															*/
/*		x,y で指定した位置に正方形を１個表示する、色指定もできる				*/
/********************************************************************************/
void rush_koma_disp(int x, int y, int color)
{
	lcd_clearA(80+20*x, 20*y+60, 80+19+20*x, 19+20*y+60, color);
}
/********************************************************************************/
/*		rush_disp_ban															*/
/*		盤を表示する															*/
/********************************************************************************/
void rush_disp_ban()
{
	int x, y, num, car;
	
	for(y=0; y<8; y++){
		for(x=0; x<8; x++){
			car = (rush_ban[y][x] >> 5) & 7;
			num = rush_ban[y][x] & 0x1f;
			if(rush_ban[y][x]){
				if(car <= 4){
					rush_koma_disp(x, y, WHITE);
				}
				else{					// 赤い車
					rush_koma_disp(x, y, GRAY);	// 赤
				}
			}
		}
	}
	for(y=1; y<7; y++){
		for(x=0; x<7; x++){
			if(rush_ban[y][x] != rush_ban[y][x+1])
				lcd_clearA(80+19+20*x, 20*y+60, 80+21+20*x, 19+20*y+60, BLACK);
		}
	}
	for(x=1; x<7; x++){
		for(y=0; y<7; y++){
			if(rush_ban[y][x] != rush_ban[y+1][x])
				lcd_clearA(80+20*x, 19+20*y+60, 80+19+20*x, 21+20*y+60, BLACK);
		}
	}
}
/********************************************************************************/
/*		rush_disp_car															*/
/*		車を１台表示する														*/
/*		car: 1〜6 の車番号														*/
/*		str: 全角１文字															*/
/********************************************************************************/
void rush_disp_car(int x, int y, int car, int tooshi)
{
	int x1, y1, car1;
	
	for(y1=0; y1<3; y1++){
		for(x1=0; x1<3; x1++){
			if(car <= 4){
				if(rush_car_pat[car][y1][x1]){
					rush_koma_disp(x+x1, y+y1, WHITE);
				}
			}
			else{					// 赤い車
				car1 = car==5 ? 2 : 4;
				if(rush_car_pat[car1][y1][x1]){
					rush_koma_disp(x+x1, y+y1, GRAY);	// 背景色　赤
				}
			}
		}
	}
}
/********************************************************************************/
/*		rush_make_param															*/
/*		x,y 位置の駒から car_numb と tooshi_bango を作る						*/
/********************************************************************************/
void rush_make_param(int x, int y, int *p_car_numb, int *p_tooshi_bango)
{
	*p_car_numb = (rush_ban[y][x] >> 5) & 7;
	*p_tooshi_bango = rush_ban[y][x] & 0x1f;
}
/********************************************************************************/
/*		rush_put_car_test														*/
/*		return 1: 置ける、0:置けない											*/
/********************************************************************************/
int rush_put_car_test(char ban[8][8], int x, int y, int car_numb)
{
	int x1, y1;
	
	for(y1=0; y1<3; y1++){
		for(x1=0; x1<3; x1++){
			if(ban[y+y1][x+x1] && rush_car_pat[car_numb][y1][x1])
				return 0;				// 置けない
		}
	}
	return 1;							// 置ける
}
